<p>&sect;10 der HDL-Spielordnung lautet:</p>
<p><b><i>10. Sonstiges</i></b><i><br>
  (1) F&uuml;r s&auml;mtliche Punkte, die in dieser Spielordnung nicht geregelt 
  sind, gelten die entsprechenden Regelungen aus der Spielordnung des Deutschen 
  Dart Verbandes in der jeweils g&uuml;ltigen Fassung.</i></p>
<p>Im folgenden deshalb einige Ausz&uuml;ge aus dem umfangreichen Regelwerk des 
  deutschen Dart Verbandes DDV, Stand&nbsp;10.&nbsp;Nov.&nbsp;2002:</p>
<p align="center"><font size="+2"><b>Ligaspielordnung</b></font><br>
  <font size="+1">Bundesliga</font><br>
  <b>LSO</b></p>
<p><b>Allgemeine Spielbedingungen</b></p>
<p><b> &sect;3</b> Die Beschaffenheit der Boardanlage ist der SpWO des DDV zu 
  entnehmen. Die jeweiligen Spielst&auml;tten m&uuml;ssen mit zwei Boards ausgestattet 
  sein, die i.d.R. von den Teams gleichzeitig bespielt werden.<br>
  Zus&auml;tzlich muss ein Practice-Board zur Verf&uuml;gung stehen. Sitzpl&auml;tze 
  m&uuml;ssen f&uuml;r mind. 30 Personen vorhanden sein.</p>
<p><b>&sect;4</b> Bei allen Spielen der Bundesliga sind das Rauchen sowie das 
  Trinken von Alkohol allen Spielern w&auml;hrend ihres Spieles untersagt. Das 
  gleiche gilt auch f&uuml;r den Schreiber.</p>
<p><b>Strafen</b></p>
<p><b>&sect;24</b> (...) Ein Team, das f&uuml;r schuldig befunden wird, vors&auml;tzlich 
  (...) ein Match verloren zu haben, wird f&uuml;r den weiteren Ligabetrieb des 
  DDV gesperrt. (...)</p>
<p align="center"><b><font size="+2">Sport- und Wettkampfordnung</font></b><br>
  <b>SpWO</b></p>
<p align="left"><b><font size="+1">Teil O Begriffsdefinitionen</font></b></p>
<p align="left"><b>Leg<br>
  </b>Element eines Sets (z.B. 301, 501, 1001 etc.).</p>
<p align="left"><b>Set</b><br>
  Ein Set besteht aus mehreren Legs. Es gilt dann als gewonnen, wenn ein Spieler 
  oder eine Mannschaft eine bestimmte Anzahl von Legs gewonnen hat.</p>
<p align="left"><b>Match</b><br>
  Die Anzahl von Sets, die zwischen zwei Spielern oder Mannschaften ausgetragen 
  wird.</p>
<p align="left"><b><font size="+1">Teil II Allgemeine Spielregeln</font></b></p>
<p align="left"><b>&sect;3 Grunds&auml;tzliche Spielregelungen<br>
  </b>(4) Alle Spieler m&uuml;ssen Darts benutzen, die nicht l&auml;nger als 30,5cm 
  und nicht schwerer als 50g sein d&uuml;rfen. Jeder Dart muss aus einer Metallspitze, 
  einem Wurfk&ouml;rper, einem Schaft und einem Flight bestehen.<br>
  (5) Die Spieler haben das Recht, eine &Uuml;berpr&uuml;fung der H&ouml;he des 
  Boards und der Entfernung der Standleiste vom Board zu verlangen.</p>
<p><b>&sect;4 Wurf<br>
  </b>(1) Alle Darts m&uuml;ssen vors&auml;tzlich nacheinander mit der Hand des 
  Spielers geworfen werden.<br>
  (2) Ein Wurf besteht aus drei Darts, es sei denn, ein Leg, Set oder Match kann 
  mit weniger Darts beendet werden.<br>
  (3) Jeder Dart, der aus dem Board f&auml;llt oder abprallt, darf nicht wieder 
  geworfen werden.<br>
  (4) Solange ein Spieler sich im Wurfbereich befindet, ist es seinem Gegner nicht 
  gestattet, eine wurffertige Haltung einzunehmen.</p>
<p><b>&sect;6 Punkte (Scores)<br>
  </b>(4) Nachdem die Darts aus dem Board gezogen wurden ist ein Protest bez&uuml;glich 
  der erzielten Punkte nicht mehr zul&auml;ssig.<br>
  (5) Jede Punktzahl und jede Subtraktion muss vom Schiedsrichter, Schreiber und 
  Spieler nach jedem Wurf gepr&uuml;ft werden. Dies muss vor dem n&auml;chsten 
  Wurf geschehen. &Uuml;berpr&uuml;fungen bez&uuml;glich der notierten Punkte 
  und Subtraktionen m&uuml;ssen durchgef&uuml;hrt werden, bevor der Spieler wieder 
  wirft.<br>
  (6) Der Punktestand muss klar und leserlich in Sichtweite vor dem Spieler und 
  Schiedsrichter auf einem Punktezettel oder einer Punktetafel notiert werden.<br>
  (7) Das ben&ouml;tigte Doppel darf weder vom Schreiber noch vom Schiedsrichter 
  abweichend vom tats&auml;chlichen Wert bezeichnet werden (z.B. nicht Doppel 
  16 sondern 32).</p>
<p><b>&sect;7 Dartboards<br>
  </b>(1) Alle Dartboards m&uuml;ssen vom Typ Bristle sein.<br>
  (8) Das Dartboard muss so befestigt sein, dass es die senkrechte H&ouml;he von 
  der Mitte des Bullseye bis zu einem Punkt auf dem Boden, der gleicher H&ouml;he 
  liegt wie der Abwurfpunkt hinter der Standleiste, 173cm misst.<br>
  (11) Jeder Spieler oder Mannschaftsf&uuml;hrer hat das Recht darum zu bitten, 
  ein Board auszuwechseln oder die Segmente zu verdrehen sowie die Position des 
  Boards zu korrigieren. Voraussetzung ist das Einverst&auml;ndnis des Gegners. 
  Sollte eine Einigung nicht erzielt werden, kann der Schiedsrichter angerufen 
  werden. Dieses kann jedoch nur vor einem Match geschehen.</p>
<p><b>&sect;8 Licht<br>
  </b>(1) Bei Wettk&auml;mpfen muss jedes Board mit mindestens 400 Lux ausgeleuchtet 
  werden, wobei darauf zu achten ist, dass eine blendfreie Ausleuchtung gew&auml;hrleistet 
  ist.<br>
  (2) Dartboards, die f&uuml;r Endspiele auf B&uuml;hnen aufgestellt werden, m&uuml;ssen 
  mindestens durch zwei Strahler zu je 100 Watt beleuchtet werden.<br>
  (3) Bei B&uuml;hnenendspielen kann auch Spotlight oder Flutlicht benutzt werden. 
  Es muss aber darauf geachtet werden, dass keine Schatten auf dem Board zu sehen 
  sind.</p>
<p><b>&sect;9 Standleiste<br>
  </b>(1) Die Standleiste (Oche) ist mindestens 3,8m und h&ouml;chstens 5cm hoch, 
  sowie mindestens 61cm lang. Sie muss an dem Punkt der Mindestwurfentfernung 
  angebracht sein, das hei&szlig;t 2,37m von der R&uuml;ckseite der Standleiste 
  bis zu einer imagin&auml;ren Linie zum Board.<br>
  (2) Die diagonale Entfernung vom Bullseye bis zur R&uuml;ckseite der Standleiste 
  muss 2,93m betragen<br>
  (3) Wenn ein Oche (Abwurfbereich) einen erh&ouml;hten Spielbereich bildet, so 
  muss der Oche so konstruiert sein, dass er zentral zum Board steht. Die Ma&szlig;e 
  des Oche sind in diesem Fall:</p>
