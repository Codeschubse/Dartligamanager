<p align="left"><b><font size="+1">Teil III Turnier- und Wettkampfregeln</font></b></p>
<p align="left"><b>&sect;15 &Uuml;bungsw&uuml;rfe<br>
  </b>(1) Jeder Spieler hat das Recht, vor Beginn des Matches an seinem jeweiligen 
  Board maximal sechs &Uuml;bungsdarts zu werfen.<br>
  (2) An Boards, die nicht ausdr&uuml;cklich als &Uuml;bungsboards gekennzeichnet 
  wurden, sind nach Beginn des Wettkampfes &Uuml;bungsw&uuml;rfe nicht mehr gestattet.</p>
<p align="left"><b>&sect;16 Wettkampfspiele<br>
  </b>(2) Im Spielbereich d&uuml;rfen sich nur die Schiedsrichter, die Schreiber 
  sowie die Spieler aufhalten.<br>
  (3) Nur die Schiedsrichter und Schreiber d&uuml;rfen sich vor dem werfenden 
  Spieler aufhalten. Diese Personen m&uuml;ssen ihre Bewegungen w&auml;hrend des 
  Wurfes eines Spielers auf ein Minimum reduzieren.<br>
  (4) Der Gegner eines Spielers muss sich w&auml;hrend dessen Wurfes mindestens 
  61cm hinter diesem aufhalten.<br>
  (6) W&auml;hrend des Matches m&uuml;ssen sich alle Spieler ruhig verhalten. 
  Nur der werfende Spieler darf Fragen an den Schiedsrichter stellen. Zwischenrufe 
  von anderen Spielern, Zuschauern oder Offiziellen sind zu unterlassen.<br>
  (8) Der werfende Spieler kann den Schiedsrichter &uuml;ber die H&ouml;he seiner 
  Punktezahl oder dar&uuml;ber befragen, wie hoch seine Restpunktzahl ist. Er 
  darf jedoch nicht gesagt bekommen, wie Schluss zu machen ist.<br>
  (9) Alle Fragen, die die Punktzahl und den Punktabzug betreffen, m&uuml;ssen 
  gekl&auml;rt werden, bevor der Spieler wieder zu werfen beginnt.<br>
  (10) Nach Beendigung eines Legs, Sets oder Matches sind Beanstandungen bez&uuml;glich 
  des Punktestandes oder der Subtraktion unzul&auml;ssig.<br>
  (12) Proteste m&uuml;ssen sofort an den Schiedsrichter oder das Wettkampfgericht 
  gerichtet werden. Nach Beendigung eines Legs, Sets oder Matches sind Proteste 
  nicht mehr zul&auml;ssig.<br>
  (13) Tritt bei einem Spieler w&auml;hrend eines Matches ein Schaden an seinem 
  Sportger&auml;t auf oder muss der Spieler w&auml;hrend des Matches den Spielbereich 
  wegen au&szlig;ergew&ouml;hnlicher Umst&auml;nde verlassen, muss ihm dies mit 
  Zustimmung des Schiedsrichters f&uuml;r maximal 5 Minuten gew&auml;hrt werden.</p>
<p align="left"><b>&sect;17 Spielkleidung<br>
  </b>(2) Bei allen Spielen ist es nicht erlaubt Kopfbedeckungen, Walkmans oder 
  &auml;hnliches zu tragen, ohne den DDV bzw. die Organisatoren vorher um Erlaubnis 
  gebeten zu haben.</p>
