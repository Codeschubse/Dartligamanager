<p align="center"><b><font size="+2">Satzung</font></b></p>
<p><b>&sect;1 Name, Sitz, Gesch&auml;ftsjahr</b></p>
<ol>
  <li> Der Verein f&uuml;hrt den Namen &quot;Heidelberger Dart Liga e.V.&quot; 
    - abgek&uuml;rzt HDL. Er soll in das Vereinsregister eingetragen werden.</li>
  <li>Der Verein hat seinen Sitz in Heidelberg.</li>
  <li>Das Gesch&auml;ftsjahr erstreckt sich vom 01. Juli bis zum 30. Juni des 
    Folgejahres. Das erste Gesch&auml;ftsjahr endet am 30. Juni 1999.</li>
</ol>
<p><b>&sect;2 Zweck, Aufgaben und Grunds&auml;tze</b></p>
<ol>
  <li> Der Vereinszweck ist die Pflege und F&ouml;rderung des verbandsunabh&auml;ngigen 
    Steeldart-Sports im Rhein-Neckar-Dreieck. Der Satzungszweck wird insbesondere 
    durch folgende Ma&szlig;nahmen verwirklicht: Die HDL veranstaltet einen regelm&auml;&szlig;igen 
    Spielbetrieb in Form einer Steeldartliga sowie sonstige, jedermann zug&auml;ngliche 
    Veranstaltungen (offene Turniere, Ranglistenturniere etc.).<br>
    Dar&uuml;ber hinaus macht es sich der Verein zur Aufgabe, die Jugend dadurch 
    zu f&ouml;rdern, da&szlig; er den jugendlichen Nachwuchs zu sportlicher Fairne&szlig;, 
    Toleranz und Eigenverantwortung erzieht.</li>
  <li>Die HDL verfolgt ausschlie&szlig;lich und unmittelbar gemeinn&uuml;tzige 
    Zwecke im Sinne der &sect;&sect; 51 ff. AO. Die HDL ist selbstlos t&auml;tig 
    und verfolgt nicht in erster Linie eigenwirtschaftliche Zwecke. Mittel der 
    HDL d&uuml;rfen nur f&uuml;r die satzungsm&auml;&szlig;igen Zwecke verwendet 
    werden.</li>
  <li>Die Teams erhalten keine Gewinnanteile und in ihrer Eigenschaft als Mitglieder 
    der HDL auch keine sonstigen Zuwendungen aus Mitteln des Vereins. Sie erhalten 
    bei ihrem Ausscheiden oder bei Aufl&ouml;sung der HDL weder eingezahlte Beitr&auml;ge, 
    Umlagen und Spenden zur&uuml;ck, noch haben sie Anspruch auf Anteile des Vereinsverm&ouml;gens. 
    Dies gilt auch f&uuml;r die Spieler der angeschlossenen Teams</li>
  <li>Au&szlig;erdem darf keine Person durch Ausgaben, die dem gesetzten Zweck 
    der HDL fremd sind, oder durch unverh&auml;ltnism&auml;&szlig;ig hohe Verg&uuml;tungen 
    beg&uuml;nstigt werden. Alle Inhaber von Vereins&auml;mtern sind ehrenamtlich 
    t&auml;tig.</li>
  <li>Jeder Beschlu&szlig;, durch den die Satzung ge&auml;ndert worden ist, mu&szlig; 
    vor dessen Einreichung beim Register in Abschrift dem zust&auml;ndigen Finanzamt 
    vorgelegt werden.</li>
</ol>
<p><b>&sect;3 Mitgliedschaft und deren Erwerb</b></p>
<ol>
  <li>Mitglieder der HDL k&ouml;nnen nur rechtsf&auml;hige oder nichtrechtsf&auml;hige 
    Vereine (im folgenden: Teams) sein, die den Steeldart-Sport betreiben und 
    deren Spielort sich im Rhein-Neckar-Kreis, im Neckar-Odenwald-Kreis oder in 
    den St&auml;dten Heidelberg, Ludwigshafen oder Mannheim befindet.</li>
  <li>Die Aufnahme ist schriftlich zu beantragen. Der Antrag mu&szlig; die Erkl&auml;rung 
    enthalten, da&szlig; das Team die Satzung der HDL und alle auf ihrer Grundlage 
    ergangenen Beschl&uuml;sse und Ordnungen anerkennt.</li>
  <li>&Uuml;ber den Aufnahmeantrag entscheidet die Kapit&auml;nsversammlung der 
    HDL sp&auml;testens zu Beginn ihrer n&auml;chsten ordentlichen Sitzung. Die 
    Ablehnung des Aufnahmeantrags, die keiner Begr&uuml;ndung bedarf, ist unanfechtbar.</li>
  <li>Nat&uuml;rliche oder juristische Personen, die sich um die F&ouml;rderung 
    des Steeldart-Sports oder die Ziele der HDL besonders verdient gemacht haben, 
    k&ouml;nnen auf Beschlu&szlig; der Kapit&auml;nsversammlung zu Ehrenmitgliedern 
    ernannt werden. Das Ehrenmitglied soll innerhalb eines Monats erkl&auml;ren, 
    ob es die Ehrenmitgliedschaft annimmt.</li>
</ol>
<p><b>&sect;4 Rechte und Pflichten der Teams und ihrer Spieler</b></p>
<ol>
  <li>Die Teams und ihre Spieler sind berechtigt, an allen Veranstaltungen der 
    HDL teilzunehmen.</li>
  <li>Die Teams und ihre Spieler sind verpflichtet, die Zwecke der HDL zu f&ouml;rdern 
    und alles zu unterlassen, was das Ansehenn der HDL gef&auml;hrden k&ouml;nnte. 
    Sie haben Verst&ouml;&szlig;e gegen diese Satzung sowie der auf ihrer Grundlage 
    ergangenen Ordnungen zu vermeiden. Sie sind weiter verpflichtet, Einzelweisungen 
    der Organe der HDL zu befolgen, sofern diese nicht im Widerspruch zum Recht 
    der HDL stehen.</li>
  <li>Die Teams &uuml;ben ihre Mitgliedschaftsrechte in der Kapit&auml;nsversammlung 
    durch ihre Kapit&auml;ne oder deren Stellvertreter als Delegierte aus. Diese 
    &uuml;ben ihr Stimmrecht nach bestem Wissen und Gewissen aus. Sie sind an 
    Weisungen der sie entsendenden Teams nicht gebunden.</li>
  <li>Jedes Team hat das Recht, Antr&auml;ge, Anfragen, Vorschl&auml;ge oder Beschwerden 
    beim Vorstand der HDL einzureichen. Ferner k&ouml;nnen die Teams Ausk&uuml;nfte 
    &uuml;ber Angelegenheiten der HDL verlangen; dieses Recht darf jedoch nicht 
    mi&szlig;br&auml;uchlich ausge&uuml;bt werden; dies w&auml;re der Fall, wenn 
    in einer nahe bevorstehenden Kapit&auml;nsversammlung die gew&uuml;nschte 
    Auskunft erlangt werden kann.</li>
  <li>Jedes Team hat einen Jahresbeitrag im voraus zu leisten. Die H&ouml;he der 
    Beitr&auml;ge wird in einer von der Kapit&auml;nsversammlung zu beschlie&szlig;enden 
    Geb&uuml;hrenordnung festgesetzt, die auch den Zeitpunkt der F&auml;lligkeit 
    regelt. Die Beitr&auml;ge k&ouml;nnen auch nach der Spielerzahl der einzelnen 
    Teams gestaffelt werden. Bei einem nicht vorhersehbaren Finanzbedarf der HDL 
    kann die Kapit&auml;nsversammlung die Erhebung einer Umlage beschlie&szlig;en, 
    die das F&uuml;nffache des Jahresbeitrags nicht &uuml;berschreiten darf. Gezahlte 
    Beitr&auml;ge oder Umlagen werden nicht zur&uuml;ckerstattet, auch nicht bei 
    Austritt oder Zur&uuml;ckziehung einer Mannschaft w&auml;hrend des Gesch&auml;ftsjahres. 
    Ehrenmitglieder treffen keine finanziellen Beitragspflichten.</li>
  <li>Teams, die ihrer finanziellen Beitragspflicht nicht nachgekommen sind, k&ouml;nnen 
    bis zur vollst&auml;ndigen Zahlung keinerlei Mitgliedschaftsrechte aus&uuml;ben. 
    Das Gleiche gilt, wenn ein Team trotz zweimaliger Aufforderung seinen sonstigen 
    Satzungsm&auml;&szlig;igen Pflichten nicht nachkommt. Das Ruhen der Mitgliedschaftsrechte 
    wird vom Vorstand festgestellt. Das Ende des Ruhens der Mitgliedschaftsrechte 
    wird dem Team vom Vorstand formlos bekanntgemacht.</li>
  <li>Die Teams legen dem Vorstand der HDL mit der Meldung eine Spielerliste vor. 
    Ebenso sind alle &Auml;nderungen im laufenden Gesch&auml;ftsjahr, insbesondere 
    bei den Kapit&auml;nen oder deren Anschriften sowie bei den Spiellokalen, 
    schriftlich mitzuteilen.</li>
  <li>Die Teams k&ouml;nnen auch mehrere Mannschaften melden; jeder Spieler darf 
    nur in einem Team gemeldet sein. F&uuml;r jede Mannschaft ist ein gesonderter 
    Kapit&auml;n zu benennen. Einzelheiten regelt die Spielordnung.</li>
</ol>
<p><b>&sect;5 Beendigung der Mitgliedschaft</b></p>
<ol>
  <li>Die Mitgliedschaft endet mit dem Austritt oder dem Ausschlu&szlig; eines 
    Teams aus der HDL. Die Mitgliedschaft endet au&szlig;erdem, wenn sich ein 
    Team oder die HDL selbst aufl&ouml;sen oder wenn ein Team seinen Spielort 
    ohne vorherige Genehmigung der Kapit&auml;nsversammlung an einen Ort au&szlig;erhalb 
    des in &sect; 3.1 dieser Satzung genannten Gebietes verlegt.</li>
  <li>Der Austritt mu&szlig; vom Team durch seinen Kapit&auml;n schriftlich mit 
    einer K&uuml;ndigungsfrist von einem Monat zum Ende des Gesch&auml;ftsjahres 
    gegen&uuml;ber dem Vorstand der HDL erkl&auml;rt werden. W&auml;hrend des 
    Laufs der K&uuml;ndigungsfrist ist die R&uuml;cknahme der Austrittserkl&auml;rung 
    zul&auml;ssig.</li>
  <li>F&uuml;r den Ausschlu&szlig; eines Teams gilt &sect; 14.2 dieser Satzung.</li>
</ol>
<p><b>&sect;6 Organe</b></p>
<ol>
  <li>Die Organe der HDL sind der Vorstand und die Kapit&auml;nsversammlung.</li>
</ol>
<p><b>&sect;7 Aufgaben der Kapit&auml;nsversammlung</b></p>
<ol>
  <li>Die Kapit&auml;nsversammlung ist das oberste Organ der HDL. Sie trifft alle 
    Entscheidungen in der HDL, soweit diese nicht ausdr&uuml;cklich auf den Vorstand 
    &uuml;bertragen wurden. Sie kann dem Vorstand, allen Teams und deren Spielern 
    Weisungen erteilen.</li>
  <li>Die Kapit&auml;nsversammlung ist insbesondere zust&auml;ndig f&uuml;r:<br>
    a. Die Aufnahme neuer Teams in die HDL.<br>
    b. Die Feststellung der ordnungsgem&auml;&szlig;en Einberufung der Kapit&auml;nsversammlung 
    und ihrer Beschlu&szlig;f&auml;higkeit.<br>
    c. Die Entgegennahme der Jahresberichte der Vorstandsmitglieder und Kassenpr&uuml;fer.<br>
    d. Die Genehmigung des Jahresabschlusses und die Entlastung des Vorstands 
    bzw. ihre Verweigerung.<br>
    e. Die Wahl der Mitglieder des Vorstands.<br>
    f. Die Wahl der Kassenpr&uuml;fer.<br>
    g. Die Festsetzung der Beitr&auml;ge, Geb&uuml;hren und Umlagen nach &sect; 
    4.5 dieser Satzung.<br>
    h. Die Aufstellung der in dieser Satzung, insbesondere in &sect; 13, vorgesehenen 
    Ordnungen.<br>
    i. Die &Uuml;bertragung von Aufgaben der Kapit&auml;nsversammlung auf den 
    Vorstand.<br>
    j. Die &Auml;nderung dieser Satzung.<br>
    k. Die Entscheidung &uuml;ber die Berufung gegen Teamausschl&uuml;sse und 
    &auml;hnliche Sanktionen nach &sect; 14 dieser Satzung.<br>
    l. Die Beschlu&szlig;fassung &uuml;ber die Aufl&ouml;sung der HDL.</li>
</ol>
<p><b>&sect; 8 Einberufung der ordentlichen Kapit&auml;nsversammlung</b></p>
<ol>
  <li>Die Kapit&auml;nsversammlung findet einmal j&auml;hrlich im August statt.</li>
  <li>Die Teams werden schriftlich vom Vorstand eingeladen und zwar unter der 
    der HDL zuletzt bekanntgegebenen Anschrift des Kapit&auml;ns. In der Einladung 
    sind Ort und Zeit der Kapit&auml;nsversammlung anzugeben. Es soll auch die 
    (vorl&auml;ufige) Tagesordnung angegeben sein. Die Einladungen sind so rechtzeitig 
    abzusenden, da&szlig; sie die Kapit&auml;ne sp&auml;testens vier Wochen vor 
    der Versammlung erreichen.</li>
  <li>Jedes Team kann bis sp&auml;testens zwei Wochen vor einer Kapit&auml;nsversammlung 
    schriftlich beim Vorstand die Erg&auml;nzung der Tagesordnung verlangen. &Uuml;ber 
    die Erg&auml;nzung sollen die Kapit&auml;ne noch vor der Kapit&auml;nsversammlung 
    in der Form verst&auml;ndigt werden, wie sie geladen worden sind. Ist dies 
    nicht mehr m&ouml;glich, so hat der Vorstand die Erg&auml;nzung zu Beginn 
    der Kapit&auml;nsversammlung bekanntzugeben. Antr&auml;ge auf Erg&auml;nzung 
    der Tagesordnung k&ouml;nnen in der Kapit&auml;nsversammlung als Dringlichkeitsantr&auml;ge 
    gestellt werden; die Behandlung erfordert jedoch eine Zweidrittelmehrheit.</li>
</ol>
<p><b>&sect; 9 Einberufung der au&szlig;erordentlichen Kapit&auml;nsversammlung</b></p>
<ol>
  <li>Der Vorstand kann eine au&szlig;erordentliche Kapit&auml;nsversammlung einberufen.</li>
  <li>Hierzu ist er verpflichtet, wenn es das Interesse der HDL erfordert oder 
    die Einberufung von mindestens einem Drittel der Teams unter Angabe des Zwecks 
    und des Grundes gegen&uuml;ber dem Vorstand schriftlich verlangt wird.</li>
  <li>F&uuml;r Form und Frist der Einberufung der au&szlig;erordentlichen Kapit&auml;nsversammlung 
    gilt &sect; 8.2 und &sect; 8.3 dieser Satzung entsprechend. Im Einverst&auml;ndnis 
    mit den Kapit&auml;nen aller Teams kann bei einer au&szlig;erordentlichen 
    Kapit&auml;nsversammlung die Ladungsfrist verk&uuml;rzt und auf die Schriftform 
    verzichtet werden; in diesem Fall ist der Beratungsgegenstand und die Tagesordnung 
    (fern-)m&uuml;ndlich vor Beginn der au&szlig;erordentlichen Kapit&auml;nsversammlung 
    bekanntzugeben.</li>
</ol>
<p><b>&sect; 10 Ablauf der Kapit&auml;nsversammlung</b></p>
<ol>
  <li>An den Kapit&auml;nsversammlungen k&ouml;nnen alle Spieler der Teams teilnehmen. 
    &uuml;ber die Zulassung von G&auml;sten entscheidet die Kapit&auml;nsversammlung.</li>
  <li>Die Kapit&auml;nsversammlung ist ohne R&uuml;cksicht auf die Zahl der erschienenen 
    Kapit&auml;ne beschlu&szlig;f&auml;hig. Bei Abstimmung &uuml;ber die Aufl&ouml;sung 
    der HDL und &uuml;ber Satzungs&auml;nderungen ist die Kapit&auml;nsversammlung 
    nur beschlu&szlig;f&auml;hig, wenn mindestens die H&auml;lfte der Stimmberechtigten 
    Kapit&auml;ne anwesend ist.</li>
  <li>Regelungen zum Stimmrecht in der Kapit&auml;nsversammlung:<br>
    a. Jedes Team hat mindestens eine Stimme.<br>
    b. Hat ein Team mehr als eine Mannschaft gemeldet, so erh&auml;lt es eine 
    weitere Stimme f&uuml;r jede weitere Mannschaft, wenn die Meldung wenigstens 
    eine Woche vor der Kapit&auml;nsversammlung beim Vorstand eingegangen ist 
    und der Vorstand die Meldung f&uuml;r mit der Spielordnung vereinbar erkl&auml;rt 
    hat. In Zweifelsf&auml;llen entscheidet die Kapit&auml;nsversammlung zu Beginn 
    ihrer Sitzung.<br>
    c. Vorstandsmitglieder haben als solche kein Stimmrecht.<br>
    d. Ehrenmitglieder haben kein Stimmrecht.<br>
    e. Kein Anwesender kann mehrere Stimmen aus&uuml;ben.</li>
  <li>Regelungen zu den erforderlichen Mehrheiten:<br>
    a. Beschl&uuml;sse bed&uuml;rfen grunds&auml;tzlich der einfachen Mehrheit 
    der abgegebenen Stimmen. Bei Stimmengleichheit entscheidet der Vorstand.<br>
    b. F&uuml;r Satzungs&auml;nderungen und die Aufl&ouml;sung der HDL ist eine 
    Dreiviertelmehrheit der abgegebenen Stimmen notwendig.<br>
    c. Bei Wahlen gilt derjenige Kandidat als gew&auml;hlt, der die meisten Stimmen 
    erh&auml;lt. Bei Stimmengleichheit entscheidet das Los.<br>
    d. Enthaltungen und ung&uuml;ltige Stimmen werden nicht mitgez&auml;hlt.</li>
  <li>Als Vorstandsmitglied oder als Kassenpr&uuml;fer w&auml;hlbar ist jeder 
    in der Kapit&auml;nsversammlung anwesende Spieler eines Teams, nicht nur die 
    Kapit&auml;ne.</li>
  <li>&Uuml;ber den Verlauf der Kapit&auml;nsversammlung ist ein Ergebnisprotokoll 
    anzufertigen, das vom Versammlungsleiter und vom Protokollf&uuml;hrer zu unterschreiben 
    ist. Das Protokoll mu&szlig; enthalten:<br>
    a. Ort und Zeit der Versammlung;<br>
    b. Namen des Versammlungsleiters und des Schriftf&uuml;hrers;<br>
    c. Namen der erschienenen Kapit&auml;ne einschlie&szlig;lich der von ihnen 
    vertretenen Teams;<br>
    d. Feststellung der satzungsgem&auml;&szlig;en Einberufung und ggf. der Beschlu&szlig;f&auml;higkeit;<br>
    e. Tagesordnung;<br>
    f. die gestellten Antr&auml;ge, das Abstimmungsergebnis und eventuelle Widerspr&uuml;che 
    gegen gefa&szlig;te Beschl&uuml;sse. Ein Antrag, der eine Satzungs&auml;nderung 
    betrifft, ist w&ouml;rtlich in das Protokoll aufzunehmen.</li>
  <li>Der Vorstand kann durch (telefonische) Umfrage bei allen Kapit&auml;nen 
    in dringenden F&auml;llen vorl&auml;ufige Beschl&uuml;sse der Kapit&auml;nsversammlung 
    herbeif&uuml;hren. Satzungs&auml;nderungen oder die Aufl&ouml;sung der HDL 
    d&uuml;rfen nicht Gegenstand einer solchen Umfrage sein. &Uuml;ber das Ergebnis 
    der Umfrage ist ein unterschriebenes Protokoll anzufertigen. Der vorl&auml;ufige 
    Beschlu&szlig; ist auf der n&auml;chsten Kapit&auml;nsversammlung zu best&auml;tigen. 
    Geschieht dies nicht, so sind die bereits zu seiner Ausf&uuml;hrung getroffenen 
    Ma&szlig;nahmen so weit wie m&ouml;glich r&uuml;ckg&auml;ngig zu machen.</li>
</ol>
<p><b>&sect; 11 Vorstand</b></p>
<ol>
  <li>Der Vorstand besteht aus:<br>
    a. dem Ersten Vorsitzenden,<br>
    b. dem Stellvertretenden Vorsitzenden,<br>
    c. dem Ligaleiter,<br>
    d. dem Kassenwart und<br>
    e. dem Pressewart.</li>
  <li>Der Vorstand wird von der Kapit&auml;nsversammlung auf die Dauer eines Jahres 
    gew&auml;hlt. Er bleibt so lange im Amt, bis eine Neuwahl erfolgt. Scheidet 
    ein Vorstandsmitglied w&auml;hrend der Amtsperiode aus, w&auml;hlt der Vorstand 
    ein Ersatzmitglied f&uuml;r den Rest der Amtsdauer des ausgeschiedenen Vorstandsmitglieds.</li>
  <li>Dem Vorstand obliegt die Leitung des Vereins und die F&uuml;hrung seiner 
    Gesch&auml;fte. Er hat insbesondere folgende Aufgaben:<br>
    a. Verantwortliche Leitung des gesamten Sportbetriebes und der Organisation 
    der HDL;<br>
    b. Durchf&uuml;hrung der Beschl&uuml;sse der Kapit&auml;nsversammlung;<br>
    c. Verwaltung des Vereinsverm&ouml;gens;<br>
    d. Verwaltung der Spielerkartei einschlie&szlig;lich Ausstellung der Spielerp&auml;sse;<br>
    e. Abschlu&szlig; der Haftpflichtversicherung;<br>
    f. Einberufung und Leitung der Kapit&auml;nsversammlung;<br>
    g. Bewirkung der Eintragungen ins Vereinsregister.</li>
  <li>Entscheidungen des Vorstands werden mit einfacher Mehrheit der Vorstandsmitglieder 
    getroffen. Der Vorstand ist ohne R&uuml;cksicht auf die Zahl der erschienenen 
    Vorstandsmitglieder beschlu&szlig;f&auml;hig.</li>
  <li>Die HDL wird gerichtlich und au&szlig;ergerichtlich durch zwei Vorstandsmitglieder 
    vertreten.</li>
</ol>
<p><b>&sect; 12 Kassenpr&uuml;fer</b></p>
<ol>
  <li>Die Kapit&auml;nsversammlung w&auml;hlt f&uuml;r ein Jahr zwei Kassenpr&uuml;fer, 
    die nicht dem Vorstand der HDL angeh&ouml;ren d&uuml;rfen.</li>
  <li>Die Kassenpr&uuml;fer sollen die Ordnungsm&auml;&szlig;igkeit der Buchf&uuml;hrung 
    und der Belege der HDL sachlich und rechnerisch pr&uuml;fen, dies durch ihre 
    Unterschrift best&auml;tigen und der Kapit&auml;nsversammlung hier&uuml;ber 
    einen Bericht vorlegen. Bei vorgefundenen M&auml;ngeln m&uuml;ssen die Kassenpr&uuml;fer 
    zuvor dem Vorstand berichten. Bei Ordnungsgem&auml;&szlig;er F&uuml;hrung 
    der Kassengesch&auml;fte beantragen die Kassenpr&uuml;fer die Entlastung.</li>
</ol>
<p><b>&sect; 13 Ordnungen</b></p>
<p>Zur Durchf&uuml;hrung dieser Satzung kann die Kapit&auml;nsversammlung auf 
  Vorschlag des Vorstands geeignete Ordnungen erlassen. Auf jeden Fall sind eine 
  Spielordnung und eine Beitragsordnung zu verabschieden.</p>
<p><b>&sect; 14 Sanktionen</b></p>
<ol>
  <li>Der Vorstand kann folgende Ordnungsma&szlig;nahmen gegen die Teams insgesamt, 
    aber auch gegen einzelne Spieler verh&auml;ngen, wenn sie gegen die Satzung 
    oder die satzungsgem&auml;&szlig;en Ordnungen versto&szlig;en oder wenn sie 
    das Ansehen, die Ehre oder das Verm&ouml;gen der HDL sch&auml;digen:<br>
    a. Verweis;<br>
    b. Zeitlich begrenztes Verbot der Teilnahme an Ligabetrieb und Veranstaltungen 
    der HDL;<br>
    c. Geldbu&szlig;en, die in der Spielordnung verankert sind;<br>
    d. Zeitweises Ruhen der Mitgliedschaftsrechte nach &sect; 4 (6) dieser Satzung;<br>
    e. Dauerhafte Sperre einzelner Spieler;<br>
    f. Ausschlu&szlig; eines Teams aus der HDL.</li>
  <li>Der Ausschlu&szlig; eines Teams oder die dauerhafte Sperre eines Spielers 
    kann durch den Vorstand beschlossen werden, wenn das Team oder der Spieler<br>
    a. die Bestimmungen der Satzung, Ordnungen oder die Interessen der HDL grob 
    verletzt.<br>
    b. die Anordnungen oder Beschl&uuml;sse der Organe der HDL nicht befolgt.<br>
    c. mit der Zahlung seiner finanziellen Verpflichtungen gegen&uuml;ber der 
    HDL trotz zweimaliger schriftlicher Mahnung im R&uuml;ckstand ist.<br>
    Vor der Entscheidung &uuml;ber den Ausschlu&szlig; oder die dauerhafte Sperre 
    hat der Vorstand dem Betroffenen Gelegenheit zu geben, sich m&uuml;ndlich 
    oder schriftlich zu &auml;u&szlig;ern. Hierzu ist der Betroffene unter Einhaltung 
    einer Mindestfrist von zehn Tagen schriftlich aufzufordern. Die Entscheidung 
    &uuml;ber den Ausschlu&szlig; oder die dauerhafte Sperre ist schriftlich zu 
    begr&uuml;nden und dem Betroffenen mittels eingeschriebenen Briefs bekanntzugeben.<br>
    Gegen den Bescheid &uuml;ber den Ausschlu&szlig; steht dem betroffenen Team 
    die Berufung der Kapit&auml;nsversammlung offen. Sie ist mit Begr&uuml;ndung 
    innerhalb von zwei Wochen ab f&ouml;rmlicher Bekanntgabe des Bescheids schriftlich 
    beim Vorstand einzulegen. Bei der Entscheidung der Kapit&auml;nsversammlung 
    &uuml;ber den Ausschlu&szlig; ist das Team nicht stimmberechtigt. Die Berufung 
    gegen den Ausschlu&szlig;bescheid hat aufschiebende Wirkung.</li>
</ol>
<p><b>&sect; 15 Aufl&ouml;sung</b></p>
<ol>
  <li>Die Aufl&ouml;sung der HDL kann nur in einer Kapit&auml;nsversammlung beschlossen 
    werden, bei deren Einberufung die Beschlu&szlig;fassung &uuml;ber die Vereinsaufl&ouml;sung 
    den Teams angek&uuml;ndigt ist.</li>
  <li>F&uuml;r den Aufl&ouml;sungsbeschlu&szlig; gilt &sect; 10 (2) und (4b). 
    Die Abstimmung ist namentlich vorzunehmen.</li>
  <li>F&uuml;r den Fall der Aufl&ouml;sung werden der Erste Vorsitzende und der 
    Kassenwart zu Liquidatoren ernannt, die nur gemeinsam entscheiden und handeln 
    k&ouml;nnen. Rechte und Pflichten der Liquidatoren bestimmen sich im &uuml;brigen 
    nach den Vorschriften des B&uuml;rgerlichen Gesetzbuchs &uuml;ber die Liquidation 
    (&sect;&sect; 47 ff. BGB).</li>
  <li>Bei Aufl&ouml;sung der HDL oder bei Wegfall ihres bisherigen Zweckes f&auml;llt 
    das Verm&ouml;gen der HDL an die Stadt Heidelberg, die es unmittelbar und 
    ausschlie&szlig;lich f&uuml;r gemeinn&uuml;tzige Zwecke zur F&ouml;rderung 
    des Sports verwenden darf.</li>
</ol>
