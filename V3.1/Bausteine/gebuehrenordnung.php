<p><b>3. Teilnahme am Sommer-Cup und sonstige Veranstaltungen</b><br>
  F&uuml;r alle Veranstaltungen, die nicht unter Punkt 2 (Spielbetrieb der HDL) 
  geregelt sind, k&ouml;nnen separate Geb&uuml;hren erhoben werden. &Uuml;ber 
  die H&ouml;he der Geb&uuml;hren sowie &uuml;ber Siegpr&auml;mien, Pokale, Urkunden, 
  etc. entscheidet die Kapit&auml;nsversammlung.</p>
<p><b>4. Zahlung</b><br>
  Die Zahlung der fälligen Gebühren erfolgt nach Rechnungseingang auf das unten 
  aufgeführte Konto. Das auf der Rechnung vorgegebene Zahlungsziel ist dabei zu 
  berücksichtigen. Bei Nachmeldungen sind die Gebühren sofort zu entrichten, da 
  der Zahlungseingang gemäß Spielordnung eine der Voraussetzungen für die Erstellung 
  des Spielerpasses ist. Die Rechnung wird nachträglich zusammen mit dem Spielerpaß 
  zugestellt.</p>
