<p align="center"><b><font size="+2">Spielordnung</font></b></p>
<p align="center"><font size="+1">f&uuml;r Liga- und Pokalspiele<br>
  g&uuml;ltig ab 1. Juli 2003</font></p>
<p><b><font size="+1">1. Gültigkeit</font></b></p>
<p> (1) Diese Spielordnung ist die Neufassung der Spielordnung vom 1. September 
  2000. <br>
  (2) Es wurden alle Beschlüsse aus den seither stattfindenden Kapitänsversammlungen 
  aufgenommen. <br>
  (3) Diese Spielordnung ist ab dem 1. Juli 2002 gültig.</p>
<p><font size="+1"><b>2. Spielberechtigung und deren Erwerb</b></font></p>
<p> <b>2.1 Grundsätzliches</b><br>
  (1) Am Spielbetrieb der HDL können nur Teams und Spieler teilnehmen, für die 
  das Meldeverfahren ordnungsgemäß durchgeführt wurde und alle nachstehend genannten 
  Voraussetzungen erfüllen. </p>
<p><b>2.2. Bestimmungen für die Teams und Spieler</b><br>
  (1) Die Teams erlangen die Spielberechtigung durch korrekte Meldung des Teams 
  und der Spieler sowie Zahlung der Meldegebühren bis spätestens zu den jeweils 
  dafür festgelegten Stichtagen.<br>
  (2) Jedes Team muß über ein Spiellokal und eine ordnungsgemäße Spielanlage verfügen. 
  Die komplette Anschrift mit Telefonnummer des Spiellokals ist der Ligaleitung 
  mit der Anmeldung des Teams mitzuteilen.<br>
  (3) Jedes Team muß gegenüber der Ligaleitung einen Kapitän benennen. Die komplette 
  Anschrift mit Telefonnummer ist der Ligaleitung mit der Anmeldung des Teams 
  mitzuteilen.<br>
  (4) Spieler sind grundsätzlich nur dann berechtigt an Liga- oder Pokalspiel 
  teilzunehmen, wenn die Meldegebühr bezahlt ist und sie einen gültigen Spielerpaß 
  oder einen vorläufigen Spielerpaß vorlegen können.<br>
  (5) Der Spielerpaß ist schriftlich beim Spielleiter mit dem Formular "Nachmeldung" 
  zu beantragen. Ferner ist diesem Antrag ein Paßbild des Spielers beizulegen. 
  Erst danach und nach Eingang der in der jeweils gültigen Gebührenordnung festgelegten 
  Meldegebühr wird der Spielerpaß oder ein vorläufiger Spielerpaß ausgestellt. 
  <br>
  (6) Für Spieler, die während der Saison nachgemeldet werden gelten die Punkte 
  (4) und (5) in gleicher Weise. <br>
  (7) Wechselt ein Spieler während der laufenden Saison zu einem anderen Team, 
  so muß der Spieler dies selbst in schriftlicher Form mit dem Formular "Spielerwechsel" 
  beim Ligaleiter anzeigen und ist damit mit sofortiger Wirkung für die Teilnahme 
  am Spielbetrieb gesperrt. Die eigentliche Dauer der Sperre beträgt vier Spieltage 
  und deren Zählung beginnt mit dem Eingang des Schreibens beim Spielleiter. Für 
  die Berechnung der Dauer einer Sperre gelten die regulären Spieltage lt. Rahmenspielplan. 
  Pokalspiele zählen dabei nicht als Spieltag. Der erste mögliche Spieltag nach 
  der Wechselsperre wird vom Spielleiter durch Nachricht auf der Tabelle mitgeteilt. 
  Eine Wechselsperre wird durch das Saisonende nicht aufgehoben, sondern nur unterbrochen 
  und wird in der nächsten Saison fortgesetzt. Grundsätzlich entfällt eine Wechselsperre, 
  wenn das Team, bei dem der wechselnde Spieler gemeldet ist, aufgelöst worden 
  ist.</p>
<p><b>2.3. Verweigerung der Spielberechtigung </b><br>
  (1) Wird von Teams oder Spielern ein Punkt oder mehrere Punkte aus 2.2 nicht 
  erfüllt, so kann keine Spielberechtigung erteilt werden bzw. die Spielberechtigung 
  erlischt. <br>
  (2) Prinzipiell wird die Spielberechtigung denjenigen Spielern verweigert, die 
  alkoholisiert sind bzw. unter Einfluß anderer berauschender Mittel stehen und 
  somit den Spielbetrieb gefährden oder dem Image des Dartsports schaden.</p>
<p><font size="+1"><b>3. Gliederung der HDL</b></font></p>
<p> (1) Die HDL besteht aus einer Division, der alle gemeldeten Teams angehören.</p>
<p><b><font size="+1">4. Ligaspiele</font></b></p>
<p> <b>4.1 Spielort und Spielanlage</b><br>
  (1) Als Spielort wird das Spiellokal des Teams mit Heimrecht festgelegt. <br>
  (2) Am Spielort muß für den Spieltag eine Anlage mit mindestens zwei Boards 
  aufgebaut sein, an denen parallel gespielt werden kann. Die Anlage muß nach 
  den vorgeschriebenen Maßen errichtet, ausreichend beleuchtet und die Boards 
  in ordnungsgemässem Zustand sein. <br>
  (3) Eine Verlegung des Spielortes ist gemäß den Bestimmungen unter Punkt 8 "Verlegungen" 
  möglich.</p>
<p><b>4.2. Spieltag und Spielbeginn</b> <br>
  (1) Als allgemeiner Spieltag wird der Dienstag festgelegt. <br>
  (2) Der Spielbeginn ist jeweils um 20.00 Uhr. Falls ein pünktliches Erscheinen 
  nicht gewährleistet werden kann, hat der Kapitän des betreffenden Teams das 
  gegnerische Team bis 20.00 Uhr darüber zu informieren. <br>
  (3) Die Karenzzeit für beide Teams beträgt 15 Minuten. Ist nach dem Verstreichen 
  der Karenzzeit ein Team noch nicht gemäß Punkt 4.4.(1) spielfähig, so wird das 
  Spiel für dieses Team mit 0:10 als verloren gewertet. Den Kapitänen bleibt es 
  unbenommen für einen Spieler eine längere Karenzzeit zu vereinbaren. <br>
  (4) Eine Verlegung des Spieltages und der Spielzeit ist gemäß den Bestimmungen 
  unter Punkt 8 "Verlegungen" möglich.</p>
<p><b>4.3. Auslosung der einzelnen Spiele</b> <br>
  (1) Die Einzelbegegnungen werden durch die Kapitäne der beiden Mannschaften 
  mittels der verdeckt aufgelegten Spielerpässe ausgelost. <br>
  (2) Die Doppel werden nach Beendigung der Einzel nach dem gleichen Verfahren 
  ermittelt. <br>
  (3) Für das abschließende Mannschaftsspiel bestimmen die Kapitäne jeweils 6 
  Spieler und deren Reihenfolge beim Spiel. <br>
  (4) Bei Einsatz eines nicht spielberechtigten Spielers werden alle Spiele dieser 
  Begegnung für die betreffende Mannschaft als verloren gewertet.</p>
<p><b>4.4. Spielablauf</b> <br>
  (1) Gespielt wird mit 6er-Team. Ein Team ist nur spielfähig, wenn mindestens 
  5 Spieler anwesend sind. Wenn ein Team mit weniger als 5 Spielern antritt, so 
  wird das Spiel für dieses Team mit 0:10 als verloren gewertet. <br>
  (2) Es werden sechs Einzel und drei Doppel (501 b.o.f., straight in, double 
  out) sowie ein Mannschaftsspiel (1001, straight in, double out) gespielt. <br>
  (3) Vor Beginn eines Spiels wird von einem Spieler der Gastmannschaft und einem 
  Spieler der Heimmannschaft auf Bull geworfen. Derjenige Spieler, der dem Bull 
  am nächsten kommt beginnt das Spiel. Sind beide Spieler gleich weit vom Bull 
  entfernt, so muß erneut geworfen werden. <br>
  (4) Nach dem Wurf sagt der Spieler seinem Gegner und dem Schreiber immer den 
  Score an. Der Rest wird immer vom Schreiber ermittelt. <br>
  (5) Beendet ein Spieler ein Leg, so muß er dies seinem Gegner deutlich anzeigen 
  und seine Zustimmung abwarten. Erst danach dürfen die Darts aus dem Board gezogen 
  werden. Wartet der Spieler die Zustimmung nicht ab und der Gegner erkennt das 
  Finish nicht an, so muß er das Spiel - nach dem Wurf seines Gegners - mit dem 
  alten Spielstand wieder aufnehmen. <br>
  (6) Der Dart, mit dem das zum Finish notwendige Doppel getroffen wird beendet 
  das Leg. Werden danach noch Darts geworfen, so sind diese Würfe ohne Bedeutung.</p>
<p><b>4.5. Spielberichte</b> <br>
  (1) Auf dem Spielberichtsbogen sind die Paßnummern und die Namen der Spieler 
  sowie die Ergebnisse der einzelnen Spiele zu dokumentieren. Ebenfalls sind die 
  Namen der Spieler zu vermerken, die ein High-Finish über 100 oder einen 180er 
  geworfen haben.<br>
  (2) Der Spielberichtsbogen stellt ein Dokument über den Spielverlauf dar. Es 
  ist deshalb untersagt, daß nach der Unterschrift der beiden Kapitäne noch Änderungen 
  vorgenommen werden. Ferner dürfen auf dem Spielberichtsbogen keine Anmerkungen 
  -gleich welcher Art- gemacht werden, die nichts mit der Dokumentation des Spielverlaufes 
  zu tun haben. <br>
  (3) Die Zeilen für Bemerkungen können für Mitteilungen, Anregungen, Beanstandungen 
  oder dergleichen an den Spielleiter genutzt werden. Dabei sind auch die Bestimmungen 
  unter Punkte 6 und 7 (Ausschreitungen und Proteste) dieser Spielordnung zu beachten.<br>
  (4) Der von beiden Kapitänen unterschriebene Spielberichtsbogen muß von der 
  Heimmannschaft dem Spielleiter per Fax, e-mail oder Brief übermittelt werden. 
  Die Übermittlung muß am zweiten Tag nach dem jeweiligen Spieltag bis 12.00 Uhr 
  erfolgt sein. <br>
  (5) Geht ein Spielbericht nicht innerhalb der genannten Frist beim Spielleiter 
  ein, so wird das Spiel für die Heimmannschaft mit 0:10 als verloren gewertet 
  und der Heimmannschaft zusätzlich 2 Punkte abgezogen. Bei Verstoss gegen die 
  Bestimmungen aus 4.5.(1) und (2) wird das Spiel für die Heimmannschaft mit 0:10 
  als verloren gewertet.</p>
<p><b>4.6. Wertung und Tabelle</b> <br>
  (1) Für die Punktwertung der Spiele gilt folgendes Verfahren: gewonnenes Spiel 
  2 : 0 Punkte unentschieden 1 : 1 Punkte verlorenes Spiel 0 : 2 Punkte <br>
  (2) Für die Plazierung der Teams in der Tabelle sind die nachstehenden Kriterien 
  festgelegt worden. Diese gelten in der Reihenfolge ihrer Aufzählung: Pluspunkte, 
  gewonnene Legs, direkter Vergleich und Los.</p>
<p><font size="+1"><b>5. Pokalspiele</b></font></p>
<p>(1) Für die Pokalspiele gelten alle Punkte dieser Spielordnung ohne Einschränkungen, 
  sowie die Ergänzungen gemäß den nachstehenden Punkten. <br>
  (2) Bei Pokalspielen wird bis zum Halbfinale immer ein Hin- und ein Rückspiel 
  nach dem Spielablauf der Ligaspiele ausgetragen. Dabei hat jedes Team einmal 
  Heimrecht. Bei den Halbfinal- und Finalspielen wird nach dem KO-System (ohne 
  Rückspiel) gespielt.<br>
  (3) Für die Entscheidung welches Team in die nächste Runde einzieht, sind zuerst 
  die Punkte maßgeblich. Besteht aber nach dem Hin- und Rückspiel Punktgleichheit 
  werden die einzelnen Spiele aus den beiden Begegnungen addiert und dasjenige 
  Team, welches die meisten Spiele gewonnen hat ist in der nächsten Runde. Besteht 
  auch nach der Auswertung der Spiele noch Gleichheit, wird sofort ein weiteres 
  Mannschaftsspiel ausgetragen, um die Entscheidung herbeizuführen. Die Auszählung 
  der Legs entfällt.</p>
<p><font size="+1"><b>6. Ausschreitungen</b></font></p>
<p>(1) Ausschreitungen -gleich welcher Art- sind auf dem Spielberichtsbogen zu 
  vermerken. Des weiteren haben die Kapitäne der betroffenen Teams eine Stellungnahme 
  zu den Vorfällen beim Spielleiter in schriftlicher Form einzureichen. <br>
  (2) Diese Stellungnahme muß am dritten Tag nach dem jeweiligen Spieltag bis 
  12.00 Uhr beim Spielleiter eingegangen sein. <br>
  (3) Wird der Spielleiter über Ausschreitungen durch Dritte informiert, so ist 
  der Spielleiter berechtigt, die schriftliche Stellungnahme der Teamkapitäne 
  einzufordern. Die Stellungnahme der dazu aufgeforderten Kapitäne muß am dritten 
  Tag nach der Aufforderung bis 12.00 Uhr beim Spielleiter eingehen. <br>
  (4) Ausschreitungen werden grundsätzlich mit einer Spielsperre von mindesten 
  2 Spieltagen geahndet. Die endgültige Dauer der Sperre wird vom Spielleiter 
  verhängt und ist auch von der Schwere des Vergehens abhängig. <br>
  (5) Im Wiederholungsfall kann ein Spieler bzw. Team auch für die laufende Saison 
  gesperrt oder sogar ganz aus der Liga ausgeschlossen werden. Beim dauerhaften 
  Ausschluß eines Teams aus der HDL oder der dauerhaften Sperre eines Spielers 
  gilt das Verfahren des §14 der Satzung der HDL. <br>
  (6) Geht eine Stellungnahme verspätet beim Spielleiter ein oder wird dem Spielleiter 
  keine Stellungnahme zugestellt, so entscheidet der Spielleiter aufgrund der 
  vorhandenen Informationen. <br>
  (7) Einspruch gegen die Entscheidung des Spielleiters kann nur von Teams eingelegt 
  werden, die ihre Stellungnahme unter Wahrung der Fristen abgegeben haben. Der 
  Einspruch muß innerhalb von zwei Tagen nach Bekanntgabe der Entscheidung des 
  Spielleiters beim 1. Vorsitzenden eingehen. Der Vorstand entscheidet in einer 
  Sitzung mit einfacher Mehrheit über den Einspruch. Den Teamkapitänen der betroffenen 
  Teams bzw. den betroffenen Spielern wird die Möglichkeit eingeräumt an dieser 
  Sitzung teilzunehmen. Die Einladung hierzu wird drei Tage vor der Sitzung über 
  den Teamkapitän in schriftlicher Form zugestellt. Der Einspruch hat aufschiebende 
  Wirkung.</p>
<p><font size="+1"><b>7. Proteste</b></font></p>
<p>(1) Proteste gegen irreguläre Spielbedingungen sind bei der Heimmannschaft 
  unmittelbar nach Feststellung zu erheben. Danach haben beide Teams im Rahmen 
  ihrer Möglichkeiten gemeinsam dafür zu sorgen, daß der Zustand der irregulären 
  Bedingung beseitigt wird. Nur wenn alle Möglichkeiten zur Behebung ausgeschöpft 
  sind, kann beim Spielleiter Protest erhoben werden. <br>
  (2) Falls ein Team gegen irreguläre Spielbedingungen Protest erheben will, so 
  muß diese Absicht auf dem Spielberichtsbogen vermerkt sein, damit sowohl der 
  Kapitän des vom Protest betroffenen Teams noch am Spieltag als auch der Spielleiter 
  nach fristgerechtem Erhalt des Spielberichtsbogens in Kenntnis gesetzt sind. 
  <br>
  (3) Der Protest ist vom Teamkapitän in schriftlicher Form beim Spielleiter einzureichen. 
  Der Kapitän des vom Protest betroffenen Teams kann ebenfalls eine Stellungnahme 
  zu den Vorkommnissen abgeben. <br>
  (4) Der Protest und die Gegendarstellung müssen am dritten Tag nach dem jeweiligen 
  Spieltag bis 12.00 Uhr beim Spielleiter eingegangen sein. <br>
  (5) Wird der Spielleiter über irreguläre Spielbedingungen durch Dritte informiert, 
  so ist der Spielleiter berechtigt, die schriftliche Stellungnahme der Teamkapitäne 
  einzufordern. Die Stellungnahme der dazu aufgeforderten Kapitäne muß am dritten 
  Tag nach der Aufforderung bis 12.00 Uhr beim Spielleiter eingehen. <br>
  (6) Geht ein Protest oder eine Gegendarstellung verspätet oder gar nicht beim 
  Spielleiter ein, so entscheidet der Spielleiter aufgrund der vorhandenen Informationen. 
  <br>
  (7) Einspruch gegen die Entscheidung des Spielleiters kann nur von Teams eingelegt 
  werden, die ihre Stellungnahme unter Wahrung der Fristen abgegeben haben. Der 
  Einspruch muß innerhalb von zwei Tagen nach Bekanntgabe der Entscheidung des 
  Spielleiters beim 1. Vorsitzenden eingehen. Der Vorstand entscheidet in einer 
  Sitzung mit einfacher Mehrheit über den Einspruch. Den Teamkapitänen der betroffenen 
  Teams wird die Möglichkeit eingeräumt an dieser Sitzung teilzunehmen. Die Einladung 
  hierzu wird drei Tage vor der Sitzung über den Teamkapitän in schriftlicher 
  Form zugestellt. Der Einspruch hat aufschiebende Wirkung.</p>
<p><b><font size="+1">8. Verlegungen</font></b></p>
<p><b>8.1. Verlegung von Spielen</b> <br>
  (1) Kann ein Team am regulären Spieltag nicht zum Spiel antreten, so muß mit 
  dem gegnerischen Team noch vor dem angesetzten Spieltag eine Verlegung vereinbart 
  werden. Die Verlegung eines Spiels ist außerdem möglich, wenn beide Kapitäne 
  darüber eine Einigung erzielen. <br>
  (2) Kann ein Spiel nicht gemäss Spielplan durchgeführt werden, so ist eine Verlegung 
  möglich, wenn sich beide Kapitäne auf einen neuen Termin einigen. Das Spiel 
  muss innerhalb einer Frist von 14 Tagen nachgeholt werden. Die Berechnung der 
  Frist beginnt mit dem Tag nach dem regulären Spieltag. Eine Verlegung von Spielen 
  auf einen Zeitpunkt nach dem letzten Ligaspieltag der Saison ist nicht möglich. 
  <br>
  (3) Bereits verlegte Spiele können nur dann nochmals verlegt werden, wenn der 
  Kapitän des gegnerischen Teams zustimmt. Eine Verpflichtung zur Zustimmung für 
  die Verlegung eines bereits verlegten Spiels gibt es nicht. <br>
  (4) Die Meldung über eine Spielverlegungen -mit dem Datum des Nachholspieltages- 
  muß von der Heimmannschaft dem Spielleiter per Fax, e-mail oder Brief übermittelt 
  werden. Die Übermittlung muß am zweiten Tag nach dem regulären Spieltag bis 
  12.00 Uhr erfolgt sein. <br>
  (5) Wird diese Frist für die Meldung einer Spielverlegung nicht eingehalten, 
  so wird das Spiel wie bei einen nicht fristgerecht abgegebenen Spielberichtsbogen, 
  Punkt 4.5 (5), gewertet.</p>
<p><b>8.2. Verlegung des Spielortes</b> <br>
  (1) Besteht am Spieltag nicht die Möglichkeit das Spiel im Spiellokal des Teams 
  mit Heimrecht auszutragen, so kann das Spiel im Spiellokal des Gastteams ausgetragen 
  werden. <br>
  (2) Der Spielberichtsbogen wird vom Team mit Heimrecht ausgefüllt, wie wenn 
  es keine Verlegung des Spielortes gäbe. <br>
  (3) Eine Information des Spielleiters ist nicht notwendig.</p>
<p><b>8.3. Verlegung der Spielzeit</b> <br>
  (1) Eine Verlegung der Spielzeit ist möglich, wenn beide Kapitäne darüber eine 
  Einigung erzielen. <br>
  (2) Solange bei einer Verlegung der Spielzeit der planmäßige Spieltag beibehalten 
  wird, ist eine Information des Spielleiters nicht notwendig.</p>
<p><b>8.4. Wechsel des Spiellokals</b> <br>
  (1) Wechselt ein Team das Spiellokal, so sind Name, vollständige Adresse und 
  Telefonnummer des neuen Spiellokals unverzüglich dem Spielleiter mitzuteilen. 
  Der Spielleiter informiert danach alle Teams durch Vermerk auf der Tabelle. 
  <br>
  (2) Der Wechsel des Spiellokals muß so rechtzeitig gemeldet werden, daß zwischen 
  der Bekanntgabe und dem Eintritt des Spiellokalwechsels mindestens drei Wochen 
  liegen. <br>
  (3) Kann die Meldefrist nicht eingehalten werden, so ist das Team, welches das 
  Spiellokal wechselt, selbst für die Weitergabe der notwendigen Informationen 
  an die Spielgegner dieser drei Wochen verantwortlich. <br>
  (4) Meldet ein Team den Spiellokalwechsel nicht rechtzeitig bzw. bei kurzfristiger 
  Verlegung nicht rechtzeitig an die nächsten Spielgegner und kann ein Spielgegner 
  deshalb nicht zum Spiel antreten, so werden die Spiele für das mit Meldung in 
  Verzug geratene Team mit 0:10 als verloren gewertet.</p>
<p><b>8.5. Wechsel des Teamkapitäns und der Teamanschrift</b> <br>
  (1) Bestimmt ein Team einen neuen Kapitän oder neuen Stellvetreter, so ist dies 
  unverzüglich dem Spielleiter mitzuteilen. Der Spielleiter informiert danach 
  alle Teams durch Vermerk auf der Tabelle. <br>
  (2) Falls die Anschriften für Brief-, Fax- und e-mail-Zustellung nicht mit denen 
  des Teamkapitäns identisch sind und diese sich ändern, so ist die Änderung unverzüglich 
  dem Spielleiter mitzuteilen. <br>
  (3) Sämtliche Schreiben der Ligaleitung und auch der anderen Teams werden an 
  die bekannte Adresse des Teamkapitäns bzw. an die Postanschrift des Teams, falls 
  eine solche auf der Teammeldung angegeben wurde, geschickt. <br>
  (4) Jedes Team ist für die rechtzeitige Meldung von Adressenänderung selbst 
  verantwortlich. Für verspäten Meldungen trägt ausschließlich das mit der Meldung 
  in Verzug geratene Team die Konsequenzen.</p>
<p><font size="+1"><b>9. Rücktritt</b></font></p>
<p>(1) Tritt ein Team während der laufenden Spielzeit zurück, werden alle Spiele 
  dieses Teams nicht gewertet und das Team wird auf dem letzten Tabellenplatz 
  geführt. <br>
  (2) Ist ein Team zum Zeitpunkt des Rücktritts noch im Pokalwettbewerb vertreten, 
  so wird dieses Team auch aus dem Pokalwettbewerb gestrichen. An der Stellen 
  des zurückgetretenen Teams wird der letzte Pokalgegner wieder in den Pokalwettbewerb 
  aufgenommen.</p>
<p><b><font size="+1">10. Sonstiges</font></b></p>
<p>(1) Für sämtliche Punkte, die in dieser Spielordnung nicht geregelt sind, gelten 
  die entsprechenden Regelungen aus der Spielordnung des Deutschen Dart-Verbandes 
  in der jeweils gültigen Fassung.</p>
